package kb.keyboard.warrior.dto;

public class OrderDTO {
	
	private String userno;
	private int todo3display;
	private int memoNotice;
	private int topBottom;
	private int bottomBottom;
	
	public String getUserno() {
		return userno;
	}
	public void setUserno(String userno) {
		this.userno = userno;
	}
	public int getTodo3display() {
		return todo3display;
	}
	public void setTodo3display(int todo3display) {
		this.todo3display = todo3display;
	}
	public int getMemoNotice() {
		return memoNotice;
	}
	public void setMemoNotice(int memoNotice) {
		this.memoNotice = memoNotice;
	}
	public int getTopBottom() {
		return topBottom;
	}
	public void setTopBottom(int topBottom) {
		this.topBottom = topBottom;
	}
	public int getBottomBottom() {
		return bottomBottom;
	}
	public void setBottomBottom(int bottomBottom) {
		this.bottomBottom = bottomBottom;
	}
	
	
}
